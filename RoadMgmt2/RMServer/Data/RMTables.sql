/*
DROP TABLE CITIES
DROP TABLE ROADTRAFFICINFO
*/

CREATE TABLE CITIES(
	CITYID				INT PRIMARY KEY,
	CITYNAME			VARCHAR(30)
);

CREATE TABLE ROADTRAFFICINFO(
	CITYFROMID			INT FOREIGN KEY REFERENCES CITIES (CITYID),
	CITYTOID			INT FOREIGN KEY REFERENCES CITIES (CITYID),
	DRIVEDIST			INT,
	DIRECTDIST			INT,
	SPEEDLIMIT			INT,
	WEATHERCONDITION	INT,
	TRAFFICCONDITION	INT,
	BLOCKED				BIT,
	TIME				VARCHAR(30)
);

DROP TABLE ROUTEINFO
CREATE TABLE ROUTEINFO(
	TIME				VARCHAR(30),
	CITY1				VARCHAR(30),
	TIME1				INT,
	CITY2				VARCHAR(30),
	TIME2				INT,
	CITY3				VARCHAR(30),
	TIME3				INT,
	CITY4				VARCHAR(30),
	TIME4				INT,
	CITY5				VARCHAR(30),
	TIME5				INT,
);